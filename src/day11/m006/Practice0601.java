package day11.m006;

public class Practice0601 {

	public static void main(String[] args) {
		// 0 から 8 を引いた値を求めてください。 
		int a = 0;
		int b = 8;
		System.out.println(a - b);
		
		// 2.25 に 3 をかけた値を求めてください。 
		double c = 2.25;
		int d = 3;
		System.out.println(c * d);
		
		// 3 を 2 で割ったときの商を求めてください（小数点以下の値も求めます）。 
		int e = 3;
		int f = 2;
		System.out.println(e / (double)f);
		
		// 15 を 7 で割ったときの余りを求めてください。 
		int g = 15;
		int h = 7;
		System.out.println(g % h);
		
		// 9 と 10 を足した結果を 4 で割ったときの商を求めてください（小数点以下の値も求めます）。
		int i = 9;
		int j = 10;
		int k = 4;
		System.out.println((i + j) / (double)k);
		
	}

}
